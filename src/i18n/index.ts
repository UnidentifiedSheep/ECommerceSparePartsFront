function detectInitialLocale(): AppLocale {
  const saved = localStorage.getItem(storageKey)
  if (saved && supportedLocales.includes(saved as AppLocale)) return saved as AppLocale

  const browserLocale = navigator.language.split('-')[0]
  if (supportedLocales.includes(browserLocale as AppLocale)) return browserLocale as AppLocale

  return 'ru'
}

const currentLocale = ref<AppLocale>(detectInitialLocale())
document.documentElement.lang = currentLocale.value

function getMessage(path: string, localeValue: AppLocale): string | undefined {
  const value = path
    .split('.')
    .reduce<string | MessageTree | undefined>((acc, key) => (
      typeof acc === 'object' && acc !== null ? acc[key] : undefined
    ), messages[localeValue])

  return typeof value === 'string' ? value : undefined
}

export function t(path: string, params: Record<string, string | number> = {}): string {
  const template = getMessage(path, currentLocale.value)
    ?? getMessage(path, 'ru')
    ?? path

  return Object.entries(params).reduce(
    (result, [key, value]) => result.split(`{${key}}`).join(String(value)),
    template,
  )
}

export function setLocale(localeValue: AppLocale) {
  if (!supportedLocales.includes(localeValue)) return
  currentLocale.value = localeValue
  localStorage.setItem(storageKey, localeValue)
  document.documentElement.lang = localeValue
}

export function getCurrentLocale(): AppLocale {
  return currentLocale.value
}

export function useI18n() {
  return {
    locale: currentLocale,
    readonlyLocale: readonly(currentLocale),
    supportedLocales,
    t,
    setLocale,
  }
}
